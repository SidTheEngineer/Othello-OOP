public class Disc {
    
    private Color discColor;
    
    // "getters" and "setters" typically public to get and set values of
    // the member varibles of the object from other objects.
    public Color getDiscColor() {
        return discColor;
    }

    public void setDiscColor(Color discColor) {
        this.discColor = discColor;
    }
    
}
