package core;

import java.awt.Color;

public class Player {
    
    private String name;
    private Color discColor;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Color getDiscColor() {
        return discColor;
    }

    public void setDiscColor(Color discColor) {
        this.discColor = discColor;
    }
    
    
}
